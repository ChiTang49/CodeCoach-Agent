class HelloAgentsLLM:
    def __init__(self, model: str = None, apiKey: str = None, baseUrl: str = None, timeout: int = None):
        # å›ºå®šä½¿ç”¨ DeepSeek-Chat éæ€è€ƒæ¨¡å¼
        self.model = model or os.getenv("LLM_MODEL_ID", "deepseek-chat")
        apiKey = apiKey or os.getenv("DEEPSEEK_API_KEY")
        baseUrl = baseUrl or os.getenv("DEEPSEEK_BASE_URL")
        timeout = timeout or int(os.getenv("LLM_TIMEOUT", 60))
        
        if not all([self.model, apiKey, baseUrl]):
            raise ValueError("DeepSeek APIå¯†é’¥å’ŒæœåŠ¡åœ°å€å¿…é¡»åœ¨.envæ–‡ä»¶ä¸­é…ç½®ã€‚")

        self.client = OpenAI(api_key=apiKey, base_url=baseUrl, timeout=timeout)

    def think(self, messages: List[Dict[str, str]], temperature: float = 0) -> str:
        print(f"ğŸ§  æ­£åœ¨è°ƒç”¨ {self.model} æ¨¡å‹...")
        try:
            response = self.client.chat.completions.create(
                model=self.model,
                messages=messages,
                temperature=temperature,
                stream=True,
            )
            
            # å¤„ç†æµå¼å“åº”
            print("âœ… å¤§è¯­è¨€æ¨¡å‹å“åº”æˆåŠŸ:")
            collected_content = []
            for chunk in response:
                content = chunk.choices[0].delta.content or ""
                print(content, end="", flush=True)
                collected_content.append(content)
            print()  # åœ¨æµå¼è¾“å‡ºç»“æŸåæ¢è¡Œ
            return "".join(collected_content)

        except Exception as e:
            print(f"âŒ è°ƒç”¨LLM APIæ—¶å‘ç”Ÿé”™è¯¯: {e}")
            return None
    
    def invoke(self, messages: List[Dict[str, str]], **kwargs) -> str:
        """hello-agents å…¼å®¹æ¥å£"""
        temperature = kwargs.get('temperature', 0)
        return self.think(messages, temperature=temperature)

